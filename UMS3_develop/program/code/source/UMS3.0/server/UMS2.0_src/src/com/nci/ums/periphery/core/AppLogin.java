package com.nci.ums.periphery.core;

public class AppLogin{
	private String appID;
	private String subAppID;
	private int lostTime;
	/**
	 * @return Returns the appID.
	 */
	public String getAppID() {
		return appID;
	}
	/**
	 * @param appID The appID to set.
	 */
	public void setAppID(String appID) {
		this.appID = appID;
	}
	/**
	 * @return Returns the lostTime.
	 */
	public int getLostTime() {
		return lostTime;
	}
	/**
	 * @param lostTime The lostTime to set.
	 */
	public void setLostTime(int lostTime) {
		this.lostTime = lostTime;
	}
	/**
	 * @return Returns the subAppID.
	 */
	public String getSubAppID() {
		return subAppID;
	}
	/**
	 * @param subAppID The subAppID to set.
	 */
	public void setSubAppID(String subAppID) {
		this.subAppID = subAppID;
	}
	
	
}
